    <div class="container">
        <p>El eneagrama es un método de clasificación de la personalidad utilizado generalmente para el autoconocimiento
            y el desarrollo personal. Presenta nueve tipos de personalidad principales. Se trata de una técnica
            milenaria pero interpretada, desde el punto de vista de la psicología, por múltiples autores recientes.</p>
        <h2>Los 9 tipos de personalidad que conforman el eneagrama son los siguientes:</h2>
        <br>
        <br>
        <div class="row">
            <div class="col-lg-4">
                <img src="img/tipo1.jpg" alt="" id="tipo1img">
        <h3>1.El reformador:</h3>
        <p>El tipo idealista de sólidos principios. Las personas tipo Uno son éticas y concienzudas, poseen un fuerte
            sentido del bien y el mal. Son profesores y cruzados, se esfuerzan siempre por mejorar las cosas, pero temen
            cometer errores. Bien organizados, ordenados y meticulosos, tratan de mantener valores elevados, pero pueden
            resultar críticos y perfeccionistas. Normalmente tienen problemas de rabia e impaciencia reprimidas. En su
            mejor aspecto, el Uno sano es sabio, perceptivo, realista y noble, a la vez que moralmente heroico.</p>
</div>
 <br>
 <div class="col-lg-4">
 <img src="img/tipo2.jpg" alt="" id="tipo2img">
        <h3>2.El ayudador: </h3>
        <p>El tipo preocupado, orientado a los demás. Los Dos son comprensivos, sinceros y bondadosos; son amistosos,
            generosos y abnegados, pero también pueden ser sentimentales, aduladores y obsequiosos. Desean intimar con
            los demás y suelen hacer cosas por ellos para sentirse necesitados. Por lo general tienen problemas para
            cuidar de sí mismos y reconocer sus propias necesidades. En su mejor aspecto, el Dos sano es generoso,
            altruista y siente un amor incondicional por sí mismo y por los demás.</p>
        <br>
        </div>
        <div class="col-lg-4">
        <img src="img/tipo3.jpg" alt="" id="tipo3img">
        <h3>3.El triunfador: </h3>
        <p>El tipo adaptable y orientado al éxito. Las personas tipo Tres son seguras de sí mismas, atractivas y
            encantadoras. Ambiciosas, competentes y enérgicas, también pueden ser muy conscientes de su posición y estar
            muy motivadas por el progreso personal. Suelen preocuparse por su imagen y por lo que los demás piensan de
            ellas. Normalmente tienen problemas de adicción al trabajo y de competitividad. En su mejor aspecto, el Tres
            sano se acepta a sí mismo, es auténtico, es todo lo que aparenta ser, un modelo que inspira a otras
            personas.</p>
        <br>
</div>
<div class="row">
    <div class="col-lg-4">
    <img src="img/tipo4.jpg" alt="" id="tipo4img">
        <h3>4.El individualista: </h3>
        <p>El tipo romántico e introspectivo. Los tipos Cuatro son conscientes de sí mismos, sensibles, reservados y
            callados. Son demostrativos, sinceros y personales emocionalmente, pero también pueden ser caprichosos y
            tímidos. Se ocultan de los demás porque se sienten vulnerables o defectuosos, pero también pueden sentirse
            desdeñosos y ajenos a las formas normales de vivir. Normalmente tienen problemas de autocomplacencia y
            autocompasión. En su mejor aspecto, los tipos Cuatro sanos son inspirados y muy creativos, capaces de
            renovarse y transformar sus experiencias.</p>
        <br>
</div>
<div class="col-lg-4">
<img src="img/tipo5.jpg" alt="" id="tipo5img">
        <h3>5.El investigador: </h3>
        <p>El tipo vehemente y cerebral. Los Cinco son despabilados, perspicaces y curiosos. Son capaces de concentrarse
            y enfocar la atención en desarrollar ideas y habilidades complejas. Independientes e innovadores, es posible
            que se obsesionen con sus pensamientos y elaboraciones imaginarias. Se desligan de las cosas, pero son muy
            nerviosos y vehementes. Por lo general tienen problemas de aislamiento, excentricidad y nihilismo. En su
            mejor aspecto, el Cinco sano es pionero visionario, suele estar en la vanguardia y es capaz de ver el mundo
            de un modo totalmente nuevo.</p>
        <br>
</div>
<div class="col-lg-4">
<img src="img/tipo6.jpg" alt="" id="tipo6img">
        <h3>6.El leal: </h3>
        <p>El tipo comprometido, orientado a la seguridad. Las personas tipo Seis son dignas de confianza, trabajadoras
            y responsables, pero también pueden adoptar una actitud defensiva, ser evasivas y muy nerviosas; trabajan
            hasta estresarse al mismo tiempo que se quejan de ello. Suelen ser cautelosas e indecisas, aunque también
            reactivas, desafiantes y rebeldes. Normalmente tienen problemas de inseguridad y desconfianza. En su mejor
            aspecto, los Seis sanos son estables interiormente, seguros de sí mismos, independientes, y apoyan con
            valentía a los débiles e incapaces.</p>
        <br>
</div>
</div>
<div class="row">
    <div class="col-lg-4">
    <img src="img/tipo7.jpg" alt="" id="tipo7img">
        <h3>7.El entusiasta: </h3>
        <p>El tipo productivo y ajetreado. Los Siete son versátiles, optimistas y espontáneos; juguetones, animosos y
            prácticos, también podrían abarcar demasiado, ser desorganizados e indisciplinados. Constantemente buscan
            experiencias nuevas y estimulantes, pero la actividad continuada los aturde y agota. Por lo general tienen
            problemas de superficialidad e impulsividad. En su mejor aspecto, los Siete sanos centran sus dotes en
            objetivos dignos, son alegres, muy capacitados y muy agradecidos.</p>
        <br>
</div>
<div class="col-lg-4">
<img src="img/tipo8.jpg" alt="" id="tipo8img">
        <h3>8.El desafiador: </h3>
        <p>El tipo poderoso y dominante. Las personas tipo Ocho son seguras de sí mismas, fuertes y capaces de
            imponerse. Protectoras, ingeniosas y decididas, también resultan orgullosas y dominantes; piensan que deben
            estar al mando de su entorno y suelen volverse retadoras e intimidadoras. Normalmente tienen problemas para
            intimar con los demás. En su mejor aspecto, los Ocho sanos se controlan, usan su fuerza para mejorar la vida
            de otras personas, volviéndose heroicos, magnánimos y a veces históricamente grandiosos.</p>
        <br>
</div>
<div class="col-lg-4">
<img src="img/tipo9.jpg" alt="" id="tipo9img">
        <h3>9.El pacificador: </h3>
        <p>El tipo acomodadizo, humilde. Los tipos Nueve son conformistas, confiados y estables. Son afables,
            bondadosos, se acomodan con facilidad y ofrecen su apoyo, pero también pueden estar demasiado dispuestos a
            transigir con los demás para mantener la paz. Desean que todo vaya sobre ruedas, sin conflictos, pero
            tienden a ser complacientes y a minimizar cualquier cosa inquietante. Normalmente tienen problemas de
            pasividad y tozudez. En su mejor aspecto, los Nueve sanos son indómitos y abarcadores; son capaces de unir a
            las personas y solucionar conflictos.</p>
        <br>
</div>
